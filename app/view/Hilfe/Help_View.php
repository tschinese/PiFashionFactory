    <strong> Willkommen im FAQ </strong><br><br>

    <strong>Warum muss ich ein Benutzerprofil bei FashionFactory erstellen?</strong><br>

    <div>Bevor Sie eine Bestellung aufgeben können, müssen Sie ein Benutzerprofil erstellen. Wenn Sie sich in Ihr Benutzerprofil einloggen, sind Ihre persönlichen Daten bereits gespeichert. Sie brauchen sie also nicht noch einmal einzugeben, wenn Sie eine neue Bestellung aufgeben möchten. Sie haben auch Zugriff auf ihr Profil – dort finden Sie einen umfassenden Überblick über Ihr Profil.</div>

    <br><strong>Wie erstelle ich ein Profil bei FashionFactory?</strong>

    <div>Es ist ganz einfach, bei FashionFactory Ihr eigenes Benutzerprofil zu erstellen, indem Sie einen Benutzernamen und ein Passwort wählen. Bitte verwenden Sie als Benutzernamen Ihre E-Mail-Adresse.
        <br> Registrieren können sie sich <a href="index.php?url=Registrieren_Controller">hier</a>.</div>

    <br><strong>Ich kann mich nicht mehr einloggen. Was kann ich tun?</strong>

    <div>Wenn Sie sich nicht einloggen können, klicken Sie auf den Link <a href="index.php?url=ForgottenPassword">„Passwort vergessen”</a> – wir senden Ihnen umgehend eine E-Mail mit Ihrem neuen Passwort.</div>

    <br><strong> Wo kann ich welche Arten von Bezahlkonten anlegen?</strong>
    <div>Die akzeptierten Zahlmethoden sind: Lastschrift und Kreditkarte. Ein neues Bankkonto bzw. eine neue Kreditkarte können sie sich <a href = "index.php?url=KontoController/callView">hier</a> anlegen. Wir akzeptieren Visa und Mastercard Kreditkarten solange sie in Deutschland ausgestellt wurden.</div>
    <br><strong> Wo gebe ich beim Einkaufen meinen Rabattcode ein?</strong><br>

    <div> Stellen Sie sicher, dass Sie eingeloggt sind (als neuer Kunde müssen Sie zunächst ein Profil erstellen), öffnen Sie anschließend den Warenkorb und geben Sie Ihren Code in die entsprechende Box ein.</div>

    <br> <strong>Wie lange dauert die Lieferung?</strong>

    <div> Ihre Bestellung sollte innerhalb von 2 bis 4 tagen nach Bestellbestätigung der vorrätigen Artikel geliefert werden.</div><br>

    <strong>Wie gebe ich einen Artikel zurück?</strong>

    <div>Sie können Ihre(n) Artikel innerhalb von 14 Tagen nach Erhalt Ihres Pakets zurückgeben. Die Artikel müssen ungetragen und in einwandfreiem Zustand sein.Alle Pakete enthalten ein Rücksendeetikett und einen Lieferschein. Bitte verwenden Sie dieses Etikett, wenn Sie Artikel zurücksenden möchten. Zur Rücksendung kommen sie <a href="index.php?url=Rucksendung_Controller">hier</a>. Weitere Informationen finden sie dort.</div>
